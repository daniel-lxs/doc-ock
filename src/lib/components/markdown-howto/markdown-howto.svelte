<div class="prose h-full max-w-none p-4 dark:prose-invert">
	<h2>Markdown Guide</h2>
	<p>
		Markdown is a lightweight markup language for creating formatted text. Here's a comprehensive
		guide:
	</p>

	<h3>Basic Formatting</h3>
	<ul>
		<li><strong>Bold</strong>: <code>**text**</code></li>
		<li><em>Italic</em>: <code>*text*</code></li>
		<li><del>Strikethrough</del>: <code>~~text~~</code></li>
		<li><code>Code</code>: <code>`code`</code></li>
		<li>
			<a href="/">Link</a>: <code>[Link](https://example.com)</code>
		</li>
		<li>Image: <code>![Alt text](image.jpg)</code></li>
	</ul>

	<h3>Headers</h3>
	<pre><code
			># H1
## H2
### H3
#### H4
##### H5
###### H6</code
		></pre>

	<h3>Lists</h3>
	<p>Unordered:</p>
	<pre><code
			>- Item 1
- Item 2
  - Subitem</code
		></pre>

	<p>Ordered:</p>
	<pre><code
			>1. First item
2. Second item
3. Third item</code
		></pre>

	<p>Task list:</p>
	<pre><code
			>- [x] Completed task
- [ ] Incomplete task</code
		></pre>

	<h3>Code Blocks</h3>
	<pre><code
			>```javascript
function hello() &#123;
  console.log('Hello world!');
&#125;
```</code
		></pre>

	<h3>Blockquotes</h3>
	<pre><code
			>> This is a blockquote
>> Nested blockquote</code
		></pre>

	<h3>Horizontal Rule</h3>
	<pre><code>---</code></pre>

	<h3>Tables</h3>
	<pre><code
			>| Syntax | Description |
|--------|-------------|
| Header | Title       |
| Paragraph | Text     |</code
		></pre>

	<h3>Inline HTML</h3>
	<pre><code><span style="color:red">Red text</span></code></pre>

	<h3>Escaping Characters</h3>
	<pre><code>\*literal asterisks\*</code></pre>

	<p class="mt-8 text-sm text-gray-500">Based on CommonMark specification</p>
</div>
